"""
Tree-of-Thought ⇄ GraphRAG engine
═════════════════════════════════
**Purpose**

Run the ToT pipeline twice:

1. **Original question** – full sibling-queue DFS search.
2. **Null-hypothesis re-write** – the same search on the negated version
   of the question (generated by a small Rewriter agent).

Each run produces its own tree image, bundles, and Aggregator essay.
The function then:

* saves the two SVG/PNG files with a **shared timestamp** so they sort
  together, e.g.  
  `2025-06-29T14-06-12_orig_tot.png` and
  `2025-06-29T14-06-12_null_tot.png`;
* builds a single **combined markdown answer** that includes both
  questions explicitly, so legacy callers can still do  
  `result["aggregate"]["response"]`.

The full result object also exposes everything separately under
`result["original"]` and `result["null"]` for richer UIs.

Traversal strategy (inside one run)
-----------------------------------
* **Depth-first** down the *first* plan of every node.
* All sibling plans are queued FIFO, giving a *breadth* effect across
  the search space.
* At the depth ceiling we *still* run a Questioner + GraphRAG cycle, so
  every visible leaf ends in a Q/A pair.

Key functions
~~~~~~~~~~~~~
* `_dfs_path`   – one DFS path, queues siblings  
* `_search_tree` – pops that queue until exhausted / budget hit  
* `_null_question` – calls the Rewriter agent  
* `_aggregate_step` – calls the Aggregator agent  
* `run_tot`     – public façade: orchestrates two runs and ties
  everything together
"""

